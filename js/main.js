//Handle Wallet connection--------------------------------------------------------------------------
window.addEventListener('load', function() {
	updateWallet();
	updateView();
});

var web3js; // a variable to store local web3 object
let tracker, tracker2 = 0;
let account = null;
let walletAddress = null;
function updateWallet() {
  // Check if Web3 object is injected by Ethereum compatible browser such as MetaMask
  if (typeof web3 !== "undefined") {
    // Initialize Web3 using a provider of MetaMask
    web3js = new Web3(web3.currentProvider);
    //Get list of accounts
    account = web3js.eth.accounts;
    //Get currently active account if possible
    walletAddress = account[0];
    //Check if wallet is unlocked or not
    if (typeof walletAddress !== "undefined") {
      $("#status").html(walletAddress);
    }
    if (typeof walletAddress == "undefined") {
      $("#status").html("Please Unlock Wallet");
    }
  } else $("#status").html("Metamask Not Installed");
  tracker++;
  setTimeout(updateWallet, 500); // callback
}

let marginAMT = 0;
function updateView() {
	marginAMT = (0.4564687*$(window).width()) - 580;
	if (marginAMT < 10) {marginAMT = 10;}
	$('.grid-stack').css("margin-left", marginAMT);
	$('.grid-stack').css("margin-right", marginAMT);
	//$('.grid-stack').css("height", $(window).height());
	//alert($('.grid-stack').css("height"));
    tracker2++;
    setTimeout(updateView, 100); // callback
}

